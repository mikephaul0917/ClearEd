/**
 * Archive an organization.
 * Role: Institution Admin, Super Admin
 */
export const archiveOrganization = catchAsync(async (req: Request, res: Response) => {
    const { organizationId } = req.params;
    const institutionId = resolveInstitutionId(req);
    const userId = (req as any).user?.id;
    const userRole = (req as any).user?.role;

    let isAuthorized = userRole === 'admin' || userRole === 'super_admin';
    if (!isAuthorized) {
        const membership = await OrganizationMember.findOne({
            organizationId,
            userId,
            status: 'active'
        });
        if (membership) isAuthorized = true;
    }

    if (!isAuthorized) {
        throw new AppError("You don't have permission to archive this organization.", 403);
    }

    const organization = await Organization.findOne({
        _id: organizationId,
        institutionId
    });

    if (!organization) {
        throw new AppError("Organization not found.", 404);
    }

    organization.status = 'archived';
    await organization.save();

    await logAudit({
        userId,
        institutionId,
        action: 'ORGANIZATION_ARCHIVED',
        category: 'organization_management',
        resource: 'Organization',
        resourceId: organization._id as any,
        details: { name: organization.name },
        severity: 'medium',
        req
    });

    res.json({
        status: 'success',
        message: "Organization archived successfully.",
        organization
    });
});

