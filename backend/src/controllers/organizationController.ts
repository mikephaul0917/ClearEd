/**
 * Get all organizations the current user is a member of.
 */
export const getMyOrganizations = catchAsync(async (req: Request, res: Response) => {
    const userId = (req as any).user?.id;
    const institutionId = resolveInstitutionId(req);

    if (!userId || !institutionId) {
        throw new AppError("Authentication required.", 401);
    }

    const [memberships, activeTerm] = await Promise.all([
        OrganizationMember.find({
            userId,
            institutionId,
            status: 'active'
        }).populate({
            path: 'organizationId',
            populate: { path: 'termId', select: 'name academicYear semester' }
        }),
        Term.findOne({ institutionId, isActive: true })
    ]);

    const { includeArchived } = req.query;

    // CRITICAL FIX: Filter out memberships for organizations that have been deleted or archived.
    const validMemberships = memberships.filter(m => {
        if (m.organizationId === null) return false;
        if (includeArchived === 'true') return true;
        return (m.organizationId as any).status !== 'archived';
    });

    const organizations = validMemberships.map(m => {
        const org: any = m.organizationId;
        const orgObj = org.toObject() as any;

        // Smart Fallback: If organization has no termId, use the institution's active term
        if (!orgObj.termId && activeTerm) {
            const term: any = activeTerm;
            orgObj.termId = {
                _id: term._id,
                name: term.name,
                academicYear: term.academicYear,
                semester: term.semester
            };
        }

        return {
            ...orgObj,
            membership: {
                role: m.role,
                joinedAt: m.joinedAt
            }
        };
    });

    res.json({
        status: 'success',
        results: organizations.length,
        organizations
    });
});
