const resolveInstitutionId = (req: Request) => {
  const user = (req as any).user;
  if (!user) return null;
  if (user.role === 'super_admin') {
    return req.query.institutionId || req.body.institutionId || user.institutionId;
  }
  return user.institutionId;
};

/**
 * Creates a new organization within an institution.
 * Role: Institution Admin, Super Admin
 */
export const createOrganization = catchAsync(async (req: Request, res: Response) => {
    let { name, description, termId, code, signatoryName, isActive, order, isFinal } = req.body;
    const institutionId = resolveInstitutionId(req);
    const creatorId = (req as any).user?.id;

    console.log(`[ORG_CREATE] Attempting to create organization: "${name}" for institution: ${institutionId}`);

    if (!name) {
        throw new AppError("Organization name is required.", 400);
    }

    if (!institutionId) {
        throw new AppError("Institution context is required.", 401);
    }

    // 1. Resolve termId if not provided (Automatic Fallback)
    if (!termId) {
        console.log(`[ORG_CREATE] No termId provided, searching for active term...`);
        const activeTerm = await Term.findOne({ institutionId, isActive: true });
        if (activeTerm) {
            termId = activeTerm._id;
            console.log(`[ORG_CREATE] Using active term: ${activeTerm.academicYear} ${activeTerm.semester}`);
        } else {
            console.log(`[ORG_CREATE] No active term found, falling back to latest term...`);
            const latestTerm = await Term.findOne({ institutionId }).sort({ academicYear: -1, semester: -1 });
            if (latestTerm) {
                termId = latestTerm._id;
                console.log(`[ORG_CREATE] Using latest fallback term: ${latestTerm.academicYear} ${latestTerm.semester}`);
            } else {
                throw new AppError("No academic term found. Please create a Term first in System Settings.", 400);
            }
        }
    } else {
        // Verify provided termId
        const term = await Term.findOne({ _id: termId, institutionId });
        if (!term) {
            throw new AppError("Invalid term for this institution.", 400);
        }
    }

    // 2. Create the Organization
    // The joinCode is automatically generated by the model's default function
    const newOrg = await Organization.create({
        name,
        description,
        institutionId,
        termId,
        code,
        signatoryName,
        isActive: isActive !== undefined ? isActive : true,
        order: order || 0,
        isFinal: isFinal || false,
        createdBy: creatorId,
        status: "active"
    });

    // 3. Automatically add the creator as an 'officer' member
    await OrganizationMember.create({
        organizationId: newOrg._id,
        userId: creatorId,
        institutionId,
        role: "officer",
        status: "active",
        joinedAt: new Date()
    });

    // 4. Log the action
    await logAudit({
        userId: creatorId,
        institutionId,
        action: 'ORGANIZATION_CREATED',
        category: 'organization_management',
        resource: 'Organization',
        resourceId: newOrg._id as any,
        details: {
            name: newOrg.name,
            termId: newOrg.termId,
            joinCode: newOrg.joinCode
        },
        severity: 'medium',
        req
    });

    res.status(201).json({
        status: 'success',
        message: "Organization created successfully.",
        organization: newOrg
    });
});

/**
 * Get all organizations for the current institution
 */
export const getInstitutionOrganizations = catchAsync(async (req: Request, res: Response) => {
    const institutionId = resolveInstitutionId(req);
    if (!institutionId) throw new AppError("Institution context is required.", 401);

    const [organizations, activeTerm] = await Promise.all([
        Organization.find({ institutionId, status: 'active' })
            .populate('termId', 'name academicYear semester')
            .sort({ order: 1, createdAt: -1 }),
        Term.findOne({ institutionId, isActive: true })
    ]);

    const processedOrgs = organizations.map(org => {
        const orgObj = org.toObject() as any;
        if (!orgObj.termId && activeTerm) {
            const term: any = activeTerm;
            orgObj.termId = {
                _id: term._id,
                name: term.name,
                academicYear: term.academicYear,
                semester: term.semester
            };
        }
        return orgObj;
    });

    res.json({ status: 'success', organizations: processedOrgs });
});

